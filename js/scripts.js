// Convert cups to milliliters
// 1 cup = 236.588 ml
var cups = parseInt(prompt("How many cups to convert?"));

var conversion = function(cups) {
  return cups * 236.588;
}

alert(cups + " cups is equal to " + conversion(cups) + "milliliters.");

